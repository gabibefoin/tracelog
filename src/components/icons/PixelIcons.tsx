export function PixelIcon({
  name,
  size = 28,
  color = "currentColor",
}: {
  name: string;
  size?: number;
  color?: string;
}) {
  const rows = ICONS[name];
  if (!rows) return null;
  const w = rows[0].length;
  const h = rows.length;
  const rects: React.ReactNode[] = [];
  for (let y = 0; y < h; y++) {
    for (let x = 0; x < w; x++) {
      if (rows[y][x] === "#") {
        rects.push(
          <rect
            key={`${x},${y}`}
            x={x}
            y={y}
            width={1.02}
            height={1.02}
            fill={color}
          />
        );
      }
    }
  }
  return (
    <svg
      width={size}
      height={size}
      viewBox={`0 0 ${w} ${h}`}
      style={{ display: "block", shapeRendering: "crispEdges" } as React.CSSProperties}
    >
      {rects}
    </svg>
  );
}

// ── Named exports (used throughout existing codebase) ──────────────────

function makeIcon(name: string) {
  return function NamedIcon({ size = 16, color = "currentColor" }: IconProps) {
    return <PixelIcon name={name} size={size} color={color} />;
  };
}

